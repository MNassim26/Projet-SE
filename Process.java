


//Java program to implement Shortest Remaining Time First 
//Shortest Remaining Time First (SRTF) 
public class Process 
{ 
    int pid; // Process ID 
    int bt; // Burst Time 
    int art; // Arrival Time 
      
    public Process(int pid, int bt, int art) 
    { 
        this.pid = pid; 
        this.bt = bt; 
        this.art = art; 
    } 
	}
 